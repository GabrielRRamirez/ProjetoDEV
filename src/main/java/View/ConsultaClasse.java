    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String pesquisa = "";
        tabela.setNumRows(0);
        int codigo = 0;
        int tipoConsulta = 0;
        
       if(!txtNome.getText().isEmpty()){
           pesquisa = txtNome.getText();
       }
       if(!txtCodigo.getText().isEmpty()){
           codigo = Integer.parseInt(txtCodigo.getText());
       }
       if(cboPesquisa.getSelectedItem().equals("ALUNO")){
           tipoConsulta = 1;
       }else{
           tipoConsulta = 2;
       }
       
       for(CursoAluno c : controller.get(codigo, pesquisa, tipoConsulta)){
           tabela.addRow(new Object[]{
               c.getCodigo(),
               c.getNomeAluno(),
               c.getDescricaoCurso()
           });
       }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
       int linhaSelecionada = tblCursoAluno.getSelectedRow();
       
       if(linhaSelecionada >= 0){
           CursoAluno cursoAlunoselec;
           int id =(Integer.parseInt(tblCursoAluno.getValueAt(linhaSelecionada, 0).toString()));
           cursoAlunoselec = controller.get(id, "", 3).get(0);
           CadastroClasse frmCadastro = new CadastroClasse(cursoAlunoselec);
           this.getParent().add(frmCadastro);
           frmCadastro.setVisible(true);
       }else{
           JOptionPane.showMessageDialog(null, "Nenhum Registro Selecionado!");
       }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblCursoAluno.getSelectedRow();
       
       if(linhaSelecionada >= 0){
          int id =(Integer.parseInt(tblCursoAluno.getValueAt(linhaSelecionada, 0).toString()));
          controller.delete(id);
          tabela.removeRow(linhaSelecionada);
       }else{
           JOptionPane.showMessageDialog(null, "Nenhum registro selecionado!");
       }
    }//GEN-LAST:event_btnExcluirActionPerformed

