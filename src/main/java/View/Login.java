    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if (!txtLogin.getText().trim().equals("") && String.valueOf(txtSenha.getPassword()).trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Dados preenchidos incorretamente!");
            txtLogin.requestFocus();
            return;
        }            
        
        if (usuarioController.validaUsuario(txtLogin.getText().trim(), String.valueOf(txtSenha.getPassword()).trim()) > 0) {
            this.setVisible(false);
            Principal p = new Principal();
            p.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Usu√°rio ou senha incorretos!");
        }


    }//GEN-LAST:event_btnEntrarActionPerformed

